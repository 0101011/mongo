#include "wt_internal.h"

/*
 * nlpo2 --
 *	Return the next-largest power-of-two for a 32-bit unsigned value.
 */
u_int32_t
__wt_nlpo2(u_int32_t x)
{
	x |= (x >> 1);
	x |= (x >> 2);
	x |= (x >> 4);
	x |= (x >> 8);
	x |= (x >> 16);
	return (x + 1);
}
